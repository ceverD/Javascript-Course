var personToArray = function (person) {
    return [person.name, person.age, person.profession];
};
var lucas = {
    name: 'Lucas',
    age: 28,
    profession: 'Full Stack',
};
var result = personToArray(lucas);
console.log(result); // ["Lucas", 28, "Full Stack"]


